# Operadores de asignaciones
valor = 5000
# simbolo de igual

# +=
valor = valor + 1
print(valor)
# Salida = 5001

valor += 1
print(valor)
# Salida = 5002

# -=
valor = valor - 1
print(valor)
# Salida = 5001

valor -= 1
print(valor)
# Salida = 5000

# *=
valor = valor * 2
print(valor)
# Salida = 10000

valor *= 2
print(valor)
# Salida = 20000

# /=
valor = valor / 2
print(valor)
# Salida = 10000.0

valor /= 2
print(valor)
# Salida = 5000.0

# //=
valor = valor // 2
print(valor)

valor //= 2
print(valor)




